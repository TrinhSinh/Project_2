    private void jButton_xoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_xoaActionPerformed
        int chon= jTable_data.getSelectedRow();
        if(chon==-1){
          JOptionPane.showMessageDialog(rootPane, "Hay chon dong can xoa");
            return;
        }
        else{
          String id=(String) jTable_data.getValueAt(chon, 0);
          for(int i=0;i<sinhViens.size();i++){
             if(id.equals(sinhViens.get(i).getMaSo())){
               sinhViens.remove(i);
               break;
             }
          }
          tableModel.removeRow(chon);
         
        }
    }//GEN-LAST:event_jButton_xoaActionPerformed

