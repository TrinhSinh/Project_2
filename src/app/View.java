    private void jButton_themActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_themActionPerformed
        if(jTextField_ten.getText().isEmpty()||jTextField_ngaySinh.getText().isEmpty()||
                jTextField_maSo.getText().isEmpty()||jTextField_diemDauVao.getText().isEmpty()||jTextField_diaChi.getText().isEmpty()
                ){
            JOptionPane.showMessageDialog(rootPane, "Khong duoc de trong thong tin");
            return;
        }
        SinhVien t=new SinhVien();
        t.setMaSo(jTextField_maSo.getText());
        t.setTen(jTextField_ten.getText());
        t.setNgaySinh(jTextField_ngaySinh.getText());
        t.setDiaChi(jTextField_diaChi.getText());
        t.setDiemDauVao(Float.valueOf(jTextField_diemDauVao.getText()));
        int check=0;
        for(int i=0;i<sinhViens.size();i++){
           if(jTextField_maSo.getText().equals(sinhViens.get(i).getMaSo())){
             check=1;
             JOptionPane.showMessageDialog(rootPane, "Ma so da ton tai nhap ma so khac");
            return;
           }
        }
        if(check==0){
           tableModel.addRow(new Object[]{t.getMaSo(),t.getTen(),t.getNgaySinh(),t.getDiaChi(),t.getDiemDauVao()+""});
        sinhViens.add(t);
        }
        
       // tableModel.addRow(new Object[]{"eqeq","qeqe","qeqe","qeqeq","qeqe"});
    }//GEN-LAST:event_jButton_themActionPerformed

