    private void jButton_timKiemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_timKiemActionPerformed
        
        String t= JOptionPane.showInputDialog(rootPane,"Nhap ma sinh vien can tim kiem");
        
         if(t.equals("")){
           JOptionPane.showMessageDialog(rootPane, "Khong duoc de trong");
           return;
         }
           SinhVien s=new SinhVien();
           int check=0;
           for(int i=0;i<sinhViens.size();i++){
             if(t.equals(sinhViens.get(i).getMaSo())){
                   tableModel.setRowCount(0);
                   tableModel.addRow(new Object[]{sinhViens.get(i).getMaSo(),sinhViens.get(i).getTen(),sinhViens.get(i).getNgaySinh(),sinhViens.get(i).getDiaChi(),sinhViens.get(i).getDiemDauVao()+""});
                   check=1;
                break;
             }
             check=3;
           }
          if(check==0){
              JOptionPane.showMessageDialog(rootPane, "Khong tim thay trong he thong");
           return;
          }
          if(check==3){
             JOptionPane.showMessageDialog(rootPane, "Khong tim thay trong he thong");
          }
           
         
    }//GEN-LAST:event_jButton_timKiemActionPerformed

